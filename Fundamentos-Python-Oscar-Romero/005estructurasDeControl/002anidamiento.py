print("Introduce un día de la semana:")
dia = input()

if dia == 'lunes':
    print("Hoy es el peor día de la semana")
elif dia == 'martes':
    print("Hoy es el segundo peor día de la semana")
elif dia == 'miercoles':
    print("Hoy es el tercer peor día de la semana")
elif dia == 'jueves':
    print("Ya queda poco para el fin de semana")
elif dia == 'viernes':
    print("Hoy empieza el fin de semana")
elif dia == 'sabado':
    print("Hoy es el segundo mejor día de la semana")
elif dia == 'domingo':
    print("Hoy es el mejor día de la semana")
else:
    print("no has escrito un día de la semana")