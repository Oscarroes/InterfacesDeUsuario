#AND
print("Dime si es cierto:",(4>3) & (2>1) & (3>2))
print("Dime si es cierto:",(4>3) and (2>1) and (3>2))
print("Dime si es cierto:",(4>3) & (2>1) & (3>5))
#OR
print("Dime si es cierto:",(4>3) | (2>1) | (3>5))
print("Dime si es cierto:",(4>3) or (2>1) or (3>5))
print("Dime si es cierto:",(4>6) | (2>5) | (3>7))
